'use strict'

import { ArticleManagement } from './action.type'
import { articleApi } from '../api'

export const getArticleList = (page = 1, limit = 10, search)=>(dispatch)=> {
  return articleApi.getArticleList(page, limit, search).then(articleList=> {
    dispatch({
      type: ArticleManagement.GET_ARTICLE_LIST,
      data: articleList
    })
    return articleList
  })
}